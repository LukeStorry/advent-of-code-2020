tiles = {int(title[4:-1]): image for title, *image in (tile.splitlines() for tile in data.split('\n\n'))}


def adjacent(tile1, tile2):
    def borders_list(tile):
        return [tile[0], tile[-1], ''.join(row[-1] for row in tile), ''.join(row[0] for row in tile)]

    return any(
        border1 == border2 or border1 == border2[::-1]
        for border1 in borders_list(tile1) for border2 in borders_list(tile2)
    )


corners = [tile for tile, image in tiles.items() if 3 == sum(adjacent(image, other) for other in tiles.values())]
print(prod(corners))
